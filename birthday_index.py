# to prove a point
name1 = ["Grandpa",80]
name2 = ["Jake",24]

i = True

while i == True: 
    halfAge = name2[1] * 2 
    if name1[1] == halfAge:
        print("\n\n\n"+name1[0]+" will be: "+str(name1[1])+" years old, & "+name2[0]+" will be "+str(name2[1])+" years old.\n\n\n")
        i = False
    name1[1] += 1
    name2[1] += 1
